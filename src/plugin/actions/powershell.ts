import { usePluginStore, useWatchEvent } from '@/hooks/plugin.js';
import { CANVAS_SIZE, accentGradient, drawBackground, drawDivider, PALETTES } from '../canvas-style.js';

type PSRecord = {
  script: string;
  label: string;
  busy: boolean;
};

export default function (name: string) {
  const ActionID = `${window.argv[3].plugin.uuid}.${name}`;

  const plugin = usePluginStore();
  const record: Record<string, PSRecord> = {};

  // Resolve the plugin's install root from the current document URL.
  // In production index.html lives at <pluginRoot>/index.html.
  // In dev mode it's served from _.html (same folder) — both work.
  const getPluginRoot = () => {
    const href = window.location.href;
    const lastSlash = href.lastIndexOf('/');
    return lastSlash === -1 ? href : href.slice(0, lastSlash);
  };

  // Build the launcher URL for a given script base name (no extension).
  // The launcher is a .vbs wrapper (auto-generated by script/autofile.cjs)
  // that runs powershell.exe with -WindowStyle Hidden so no console window
  // ever appears on screen.
  const getLauncherUrl = (script: string) => {
    const safe = script.replace(/[^a-zA-Z0-9._-]/g, '');
    return `${getPluginRoot()}/scripts/${safe}.vbs`;
  };

  // Render the canvas for the action key.
  const canvasFunc = (context: string, status: 'idle' | 'running' | 'done' | 'error' = 'idle') => {
    const action = plugin.getAction(context);
    if (!action) return;

    const canvas = document.createElement('canvas');
    canvas.width = CANVAS_SIZE;
    canvas.height = CANVAS_SIZE;
    const ctx = canvas.getContext('2d');
    if (!ctx) return;

    let palette = PALETTES.cool;
    let badge = 'READY';
    if (status === 'running') {
      palette = PALETTES.gold;
      badge = 'RUN';
    } else if (status === 'done') {
      palette = PALETTES.green;
      badge = 'OK';
    } else if (status === 'error') {
      palette = PALETTES.rose;
      badge = 'ERR';
    }

    drawBackground(ctx, palette.bg);

    // PS prompt header
    ctx.fillStyle = 'rgba(255,255,255,0.7)';
    ctx.font = 'bold 16px "Cascadia Mono", "Consolas", monospace';
    ctx.textAlign = 'left';
    ctx.textBaseline = 'top';
    ctx.fillText('PS>', 14, 14);

    drawDivider(ctx, palette, 40);

    // Script label (centered)
    const label = (record[context]?.label || record[context]?.script || 'no script').slice(0, 14);
    ctx.fillStyle = accentGradient(ctx, palette);
    ctx.font = 'bold 18px "Segoe UI", sans-serif';
    ctx.textAlign = 'center';
    ctx.textBaseline = 'middle';
    ctx.fillText(label, 72, 80);

    // Status badge
    ctx.fillStyle = accentGradient(ctx, palette);
    ctx.font = 'bold 14px "Segoe UI", sans-serif';
    ctx.textBaseline = 'alphabetic';
    ctx.fillText(badge, 72, 128);

    action.setImage(canvas.toDataURL('image/png'));
  };

  const runScript = (context: string) => {
    const action = plugin.getAction(context);
    if (!action) return;

    const script = (record[context]?.script || '').trim();
    if (!script) {
      canvasFunc(context, 'error');
      return;
    }

    try {
      const url = getLauncherUrl(script);
      action.openUrl(url);
      record[context].busy = true;
      canvasFunc(context, 'running');

      // Reset to "done" shortly after — webview can't know real exit code.
      window.setTimeout(() => {
        if (!record[context]) return;
        record[context].busy = false;
        canvasFunc(context, 'done');
      }, 1200);
    } catch (err) {
      console.error('powershell launch error:', err);
      canvasFunc(context, 'error');
    }
  };

  const applySettings = (context: string, settings: any) => {
    const script = (settings?.script || 'SleepMonitor').toString();
    const label = (settings?.label || '').toString();
    record[context] = {
      script,
      label,
      busy: false
    };
  };

  useWatchEvent('action', {
    ActionID,
    willAppear({ context, payload }) {
      applySettings(context, payload?.settings);
      canvasFunc(context, 'idle');
    },
    willDisappear({ context }) {
      delete record[context];
    },
    didReceiveSettings({ context, payload }) {
      applySettings(context, payload?.settings);
      canvasFunc(context, 'idle');
    },
    keyUp({ context }) {
      runScript(context);
    },
    sendToPlugin({ context, payload }) {
      // Property Inspector "Test run" button.
      if (payload?.event === 'test-run') {
        runScript(context);
      }
    }
  });

  return {
    name,
    ActionID
  };
}
